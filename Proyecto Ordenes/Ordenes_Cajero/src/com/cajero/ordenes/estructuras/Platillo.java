package com.cajero.ordenes.estructuras;

public class Platillo {

	private String mPrecio,mURLImagen,mKeyValue,mNombrePlatillo;

	public String getmURLImagen() {
		return mURLImagen;
	}

	public void setmURLImagen(String mURLImagen) {
		this.mURLImagen = mURLImagen;
	}

	public String getmPrecio() {
		return mPrecio;
	}

	public void setmPrecio(String mPrecio) {
		this.mPrecio = mPrecio;
	}

	public String getmKeyValue() {
		return mKeyValue;
	}

	public void setmKeyValue(String mKeyValue) {
		this.mKeyValue = mKeyValue;
	}

	public String getmNombrePlatillo() {
		return mNombrePlatillo;
	}

	public void setmNombrePlatillo(String mNombrePlatillo) {
		this.mNombrePlatillo = mNombrePlatillo;
	}

}
